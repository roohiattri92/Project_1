package Typecasting;

public class Ptc_1 {

	public static void main(String[] args) {

		float house= 34;// implicitly
		System.out.println(house);
		
		
		float ram= (float) 33;//explicitly
		System.out.println(ram);
		
		
		int two= (int)23.87;//explicitly
	
		System.out.println(two);
		
		double g1= 23;//narrowing//
		System.out.println(g1);
		
		
		double c1= (double) 31;
		System.out.println(c1);
		
		int r1= (int) 45.83;
		System.out.println(r1);
	}

}
